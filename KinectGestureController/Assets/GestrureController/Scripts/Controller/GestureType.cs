namespace GesturesInput
{
    /// <summary>
    /// Represents the predefined gesture Ids.
    /// </summary>
    public enum GestureId
    {
        Null,
        /// <summary>
        /// Hands joined in front of chest.
        /// </summary>
        JoinedHands,

        /// <summary>
        /// Waving using the right hand.
        /// </summary>
        WaveRightHand,

        /// <summary>
        /// Waving using the left hand.
        /// </summary>
        WaveLeftHand,

        /// <summary>
        /// Swipe up using the right hand.
        /// </summary>
        SwipeUpRightHand,

        /// <summary>
        /// Swipe up using the left hand.
        /// </summary>
        SwipeUpLeftHand,

        /// <summary>
        /// Swipe down using the right hand.
        /// </summary>
        SwipeDownRightHand,

        /// <summary>
        /// Swipe down using the left hand.
        /// </summary>
        SwipeDownLeftHand,

        /// <summary>
        /// Zoom in gesture using both hands.
        /// </summary>
        ZoomIn,

        /// <summary>
        /// Zoom in gesture using both hands.
        /// </summary>
        ZoomOut,

        /// <summary>
        /// Swipe left using the right hand.
        /// </summary>
        SwipeLeftWithRightHand,

        /// <summary>
        /// Swipe left using the right hand.
        /// </summary>
        SwipeLeftWithLeftHand,

        /// <summary>
        /// Swipe right using the left hand.
        /// </summary>
        SwipeRightWithLeftHand,
        
        /// <summary>
        /// Swipe right using the left hand.
        /// </summary>
        SwipeRightWithRightHand
    }
}
